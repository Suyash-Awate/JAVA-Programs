package com.operator;

public class RelationalOperator
{
	public static void main(String [] args)
	{
		int a = 10;
		int b = 20;
		
		boolean b1 = a<b;
		System.out.println(b1);
		
		boolean b2 = a>b;
		System.out.println(b2);
		
		int c = 20;
		boolean b3 = c<=b;
		System.out.println(b3);
		
		boolean b4 = c==b;
		System.out.println(b4);
	}
}
